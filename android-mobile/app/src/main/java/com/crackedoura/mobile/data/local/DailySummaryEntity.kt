package com.crackedoura.mobile.data.local

import androidx.room.ColumnInfo
import androidx.room.Entity
import androidx.room.PrimaryKey

@Entity(tableName = "daily_summaries")
data class DailySummaryEntity(
    @PrimaryKey val day: String,
    @ColumnInfo(name = "sleep_score") val sleepScore: Int?,
    @ColumnInfo(name = "sleep_contributors_json") val sleepContributorsJson: String?,
    @ColumnInfo(name = "sleep_status") val sleepStatus: String?,
    @ColumnInfo(name = "sleep_recommendation") val sleepRecommendation: String?,
    @ColumnInfo(name = "average_spo2") val averageSpo2: Float?,
    @ColumnInfo(name = "breathing_disturbance_index") val breathingDisturbanceIndex: Int?,
    @ColumnInfo(name = "activity_score") val activityScore: Int?,
    @ColumnInfo(name = "steps") val steps: Int?,
    @ColumnInfo(name = "total_calories") val totalCalories: Int?,
    @ColumnInfo(name = "active_calories") val activeCalories: Int?,
    @ColumnInfo(name = "average_met") val averageMet: Float?,
    @ColumnInfo(name = "equivalent_walking_distance") val equivalentWalkingDistance: Int?,
    @ColumnInfo(name = "target_calories") val targetCalories: Int?,
    @ColumnInfo(name = "target_meters") val targetMeters: Int?,
    @ColumnInfo(name = "meters_to_target") val metersToTarget: Int?,
    @ColumnInfo(name = "inactivity_alerts") val inactivityAlerts: Int?,
    @ColumnInfo(name = "resting_time") val restingTime: Int?,
    @ColumnInfo(name = "sedentary_time") val sedentaryTime: Int?,
    @ColumnInfo(name = "low_activity_time") val lowActivityTime: Int?,
    @ColumnInfo(name = "medium_activity_time") val mediumActivityTime: Int?,
    @ColumnInfo(name = "high_activity_time") val highActivityTime: Int?,
    @ColumnInfo(name = "activity_contributors_json") val activityContributorsJson: String?,
    @ColumnInfo(name = "readiness_score") val readinessScore: Int?,
    @ColumnInfo(name = "readiness_contributors_json") val readinessContributorsJson: String?,
    @ColumnInfo(name = "temperature_deviation") val temperatureDeviation: Float?,
    @ColumnInfo(name = "temperature_trend_deviation") val temperatureTrendDeviation: Float?,
    @ColumnInfo(name = "stress_high") val stressHigh: Int?,
    @ColumnInfo(name = "recovery_high") val recoveryHigh: Int?,
    @ColumnInfo(name = "readiness_day_summary") val readinessDaySummary: String?,
    @ColumnInfo(name = "resilience_level") val resilienceLevel: String?,
    @ColumnInfo(name = "resilience_sleep_recovery") val resilienceSleepRecovery: Float?,
    @ColumnInfo(name = "resilience_daytime_recovery") val resilienceDaytimeRecovery: Float?,
    @ColumnInfo(name = "resilience_stress") val resilienceStress: Float?,
    @ColumnInfo(name = "vascular_age") val vascularAge: Int?,
    @ColumnInfo(name = "sleep_type") val sleepType: String?,
    @ColumnInfo(name = "sleep_start_time") val sleepStartTime: String?,
    @ColumnInfo(name = "sleep_end_time") val sleepEndTime: String?,
    @ColumnInfo(name = "bedtime_start") val bedtimeStart: String?,
    @ColumnInfo(name = "bedtime_end") val bedtimeEnd: String?,
    @ColumnInfo(name = "sleep_efficiency") val sleepEfficiency: Int?,
    @ColumnInfo(name = "total_sleep_duration") val totalSleepDuration: Int?,
    @ColumnInfo(name = "deep_sleep_duration") val deepSleepDuration: Int?,
    @ColumnInfo(name = "rem_sleep_duration") val remSleepDuration: Int?,
    @ColumnInfo(name = "light_sleep_duration") val lightSleepDuration: Int?,
    @ColumnInfo(name = "awake_time") val awakeTime: Int?,
    @ColumnInfo(name = "average_heart_rate") val averageHeartRate: Float?,
    @ColumnInfo(name = "average_hrv") val averageHrv: Int?,
    @ColumnInfo(name = "lowest_heart_rate") val lowestHeartRate: Int?,
    @ColumnInfo(name = "readiness_score_delta") val readinessScoreDelta: Float?,
    @ColumnInfo(name = "sleep_score_delta") val sleepScoreDelta: Int?,
    @ColumnInfo(name = "time_in_bed") val timeInBed: Int?,
    @ColumnInfo(name = "total_sleep_duration_all_sessions") val totalSleepDurationAllSessions: Int?,
    @ColumnInfo(name = "nap_sleep_duration") val napSleepDuration: Int?,
    @ColumnInfo(name = "sleep_session_count") val sleepSessionCount: Int?,
)
